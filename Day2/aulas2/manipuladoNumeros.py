numeroFloat = 56.7863634356

#Função round(arredondar)
print(round(numeroFloat)) #Arredonda para o proximo numero inteiro . printa o valor 
print(round(numeroFloat, 1)) #Adiciona mais um parametro, arredondando somente apos a casa decimal . printa o valor 56.8


#Especificando a quantidade de casas decimais
salario = 3245.78940
print(f'{salario:.2f}')  # :. serve para especificar a quantidade de casas decimais ira ser printado na tela, no caso somente duas casas decimais

